# -34- Écrire un programme qui affiche la table de multiplications de « 2 » (de 0
#      jusqu’à 10) en affichant : 2 * 0 = 0.

# Affichage de la table de multiplication de 2 de 0 à 10
nombre = 2
for i in range(11):
    resultat = nombre * i
    print(f"{nombre} * {i} = {resultat}")

